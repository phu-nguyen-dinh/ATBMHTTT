-- Tạo Function lấy VAITRO
CREATE OR REPLACE FUNCTION get_vaitro(v_user IN VARCHAR2)
RETURN NVARCHAR2
IS
    v_role NHANVIEN.VAITRO%TYPE;
BEGIN
    BEGIN
        SELECT VAITRO INTO v_role FROM NHANVIEN WHERE MANLD = v_user;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            BEGIN
                SELECT 'SINHVIEN' INTO v_role FROM SINHVIEN WHERE MASV = v_user;
            EXCEPTION
                WHEN NO_DATA_FOUND THEN
                    v_role := NULL;
            END;
    END;

    RETURN v_role;
END;
/

-- Tạo policy cho MOMON
CREATE OR REPLACE FUNCTION momon_policy_fn (
  schema_name VARCHAR2,
  table_name  VARCHAR2
)
RETURN VARCHAR2
AS
    v_predicate VARCHAR2(4000);
    v_user VARCHAR2(30) := SYS_CONTEXT('USERENV', 'SESSION_USER');
    v_role NVARCHAR2(30);
    v_current_hk VARCHAR2(10) := SYS_CONTEXT('APP_CTX', 'CURRENT_HK');
    v_current_nam VARCHAR2(10) := SYS_CONTEXT('APP_CTX', 'CURRENT_NAM');
BEGIN
    v_role := get_vaitro(v_user);
        
    IF v_role = 'GV' THEN
        v_predicate := 'MAGV = ''' || v_user || '''';
    ELSIF v_role = N'NV PĐT' THEN
        IF v_current_hk = 'All' AND v_current_nam = 'All' THEN
            v_predicate := '1=1';
        ELSIF v_current_hk = 'All' THEN
            v_predicate := 'NAM = ''' || v_current_nam || '''';
        ELSIF v_current_nam = 'All' THEN
            v_predicate := 'HK = ''' || v_current_hk || '''';
        ELSE
            v_predicate := 'HK = ''' || v_current_hk || ''' AND NAM = ''' || v_current_nam || '''';
        END IF;
    ELSIF v_role = N'TRGĐV' THEN
        v_predicate := 'MAGV IN (
            SELECT MANLD FROM NHANVIEN
            WHERE MADV = (
                SELECT MADV FROM DONVI
                WHERE TRGDV = ''' || v_user || '''
            )
        )';
    ELSIF v_role = 'SINHVIEN' THEN
        v_predicate := 'MAHP IN (
            SELECT MAHP FROM HOCPHAN
            WHERE MADV = (
                SELECT KHOA FROM SINHVIEN WHERE MASV = ''' || v_user || '''
            )
        )';
    ELSE
        v_predicate := '1=0';
    END IF;

    RETURN v_predicate;
END;
